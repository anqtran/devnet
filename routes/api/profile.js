// @route   POST app/profile/
// @desc    Create or edit user profile
// @access  Private
router.post(
  "/",
  passport.authenticate("jwt", { session: false }),
  (req, res) => {
    const { errors, isValid } = validateProfileInput(req.body);

    // Check Validation
    if (!isValid) {
      // Return any errors with 400 status
      return res.status(400).json(errors);
    }

    // Get fields
    const profileFiedls = {};
    profileFiedls.user = req.user.id;
    if (req.body.handle) profileFiedls.handle = req.body.handle;
    if (req.body.company) profileFiedls.company = req.body.company;
    if (req.body.website) profileFiedls.website = req.body.website;
    if (req.body.location) profileFiedls.location = req.body.location;
    if (req.body.bio) profileFiedls.bio = req.body.bio;
    if (req.body.status) profileFiedls.status = req.body.status;
    if (req.body.githubusername)
      profileFiedls.githubusername = req.body.githubusername;
    // Skills - Split into array
    if (typeof req.body.skills !== "undefined") {
      profileFiedls.skills = req.body.skills.split(",");
    }

    // Social
    profileFiedls.social = {};
    if (req.body.youtube) profileFiedls.social.youtube = req.body.youtube;
    if (req.body.twitter) profileFiedls.social.twitter = req.body.twitter;
    if (req.body.facebook) profileFiedls.social.facebook = req.body.facebook;
    if (req.body.linkedin) profileFiedls.social.linkedin = req.body.linkedin;
    if (req.body.instagram) profileFiedls.social.instagram = req.body.instagram;

    Profile.findOne({ user: req.user.id }).then(profile => {
      if (profile) {
        // Update
        Profile.findOneAndUpdate(
          { user: req.user.id },
          { $set: profileFiedls },
          { new: true }
        ).then(profile => res.json(profile));
      } else {
        // Create

        // Check if handle exists
        Profile.findOne({ handle: profileFiedls.handle }).then(profile => {
          if (profile) {
            errors.handle = "That handle already exists";
            res.status(400).json(errors);
          }

          // Save Profile
          new Profile(profileFiedls).save().then(profile => res.json(profile));
        });
      }
    });
  }
);

